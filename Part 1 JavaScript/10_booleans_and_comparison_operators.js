// true , false --> booleans

let num1 = 8;
let num2 = 8;

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= num2);
console.log(num1 <= num2);

// == & ===

let a = 22;
let b = "22";
console.log(a == b); //check only values
console.log(a === b); //check datatypes as well as values

// != & !==
console.log(a != b); //check only values
console.log(a !== b); //check datatypes as well as values
